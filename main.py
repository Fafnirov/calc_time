# Запрашиваем у пользователя количество секунд
seconds = int(input("Введите количество секунд: "))

# Считаем количество часов (в одном часе 3600 секунд)
hours = seconds // 3600

# Считаем количество минут (в одной минуте 60 секунд)
# Берём остаток от деления на 3600, чтобы убрать уже целые часы
minutes = (seconds // 60) % 60

# Остаток от деления на 60 даёт количество секунд
seconds = seconds % 60

# Выводим результат в формате часы:минуты:секунды
print(hours, ":", minutes, ":", seconds)
